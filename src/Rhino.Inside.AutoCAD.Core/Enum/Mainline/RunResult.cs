/// <summary>
/// An enum representing the result of a run.
/// </summary>
public enum RunResult
{
    /// <summary>
    /// A successful run.
    /// </summary>
    Success,

    /// <summary>
    /// An invalid run.
    /// </summary>
    Invalid,

    /// <summary>
    /// A failed run.
    /// </summary>
    Failed
}