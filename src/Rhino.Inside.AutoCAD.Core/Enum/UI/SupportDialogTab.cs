namespace Rhino.Inside.AutoCAD.Core;

/// <summary>
/// Specifies the tabs available in the Support Dialog.
/// </summary>
public enum SupportDialogTab
{
    /// <summary>
    /// The About tab with general information.
    /// </summary>
    About = 0,

    /// <summary>
    /// The Support tab with documentation and contact links.
    /// </summary>
    Support = 1,

    /// <summary>
    /// The Update tab with version information.
    /// </summary>
    Update = 2,

    /// <summary>
    /// The Settings tab for user preferences.
    /// </summary>
    Settings = 3
}